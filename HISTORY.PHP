<p><strong><em><u>Байгууллагын танилцуулга</u></em></strong>
</p>
<p style="text-align: justify;">Дархан хотын Ардын депутатын  хурлын гүйцэтгэх захиргааны Хөдөлмөр Нийгэм хангамжийн хэлтсийн харъяанд 1992 онд  анх тусламжийн сан  байгууллагдсан.   

Тусламжийн сан нь  ахмад настан, тахир дутуу иргэд, олон хүүхэдтэй өрх толгойлсон эх эцэг,  нэн ядуу, ядуу иргэдийг хамгаалах,  халуун хоолоор хооллох, эмзэг бүлгийн иргэдийн наад захын хэрэгцээг хангаж  эхэлсэн.

1995 оны Нийгмийн даатгалын шинэ хуулиар Нийгэм хангамжийн хэлтэс нь Нийгмийн даатгалын хэлтэс болсон. Халамжийн сан нь  нийгмийн даатгалын байгууллагын харъяанд үйл ажиллагаагаа явуулж байсан.

1996 оны Нийгмийн Халамжийн шинэ хууль батлагдсан бөгөөд Монгол улсын Засгийн газрын 1997 оны 2-р сарын 28-ны өдрийн 59 дүгээр тогтоолоор Улсын Нийгмийн халамжийн газар байгуулагдсан.

Монгол улсын Эрүүл мэнд Нийгмийн хамгааллын сайдын 1997 оны 7 сарын 23 ны өдрийн А/254 тоот тушаалаар Нийгмийн халамжийн үйл ажиллагааг орон нутагт эрхлэх байгууллагын бүтэц, зохион байгуулалтыг улсын хэмжээнд нэгдсэн системд оруулсан.

1998 онд “Нийгмийн халамж үйлчилгээний төв”  байгуулагдаж,   дарга,  нягтлан бодогч, тэтгэвэр,  тэтгэмжийн байцаагч, туслах ажилчин гэсэн 10 гаруй орон тоотой  үйл ажиллагаагаа явуулж байсан.

2004 онд Нийгмийн халамж үйлчилгээний төв, хөдөлмөр эрхлэлтийг алба хоёрыг нэгтгэн Хөдөлмөр халамж үйлчилгээний хэлтэс болсон.

Монгол улсын Засгийн газрын 2012 оны 09 дүгээр сарын 15-ны өдрийн 32 дугаар тогтоолоор Нийгмийн халамж үйлчилгээний хэлтэс болон өөрчлөгдөн бүтэц зохион байгуулалтын хувьд   2 алба , 42 орон тоотойгоор үйл ажиллагаагаа явуулж байна.

Нийгмийн халамж үйлчилгээний хэлтэс нь  нийгмийн халамжийн тухай хууль болон ахмад настан, хөгжлийн бэрхшээлтэй иргэний нийгмийн хамгааллын тухай хууль, Засгийн газрын тогтоол шийдвэр, бусад эрх зүйн акт, баримт бичгийг хэрэгжүүлэх, нийгмийн халамж, асрамж зайлшгүй шаардлагатай иргэний оновчтой тодорхойлох, тэдний эрэлт хэрэгцээнд нийцүүлэн төрийн үйлчилгээг хүргэж ажиллаж байна. </p>

