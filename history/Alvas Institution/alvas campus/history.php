<p>The Founder and his aspirations:</p>
<p>Alvas Education Foundation (R) was conceived and founded by a visionary chairman Dr. M. Mohan Alva, an Ayurveda Physician by profession and Philanthropist by heart. Alva's education foundation is a living dream of<br />
Dr. M. Mohan Alva to impart quality education. </p>
<p>Dr. Alvas dynamism, foresight, simplicity, goodwill, righteousness and conviction that education lies at the root of all social progress are the guiding forces behind establishing the foundation which came into being in 1995 with a few institutions affiliates to Rajiv Gandhi Health University, Bangalore.</p>
<p>Today he has a vast array of institutions in various disciplines where social transformation is becoming a reality which India was waiting for long. </p>
<p>Alva's Education Foundation (R) name is synonymous with discipline, punctuality, humility and fulfillment.<br />
